package rs.dxt.wingcommander;

public class Entity {

    protected int x, y;
    protected int r;
    
    protected int dx, dy;

    /**
     * Get x
     * 
     * @return x
     */
    public int getX() {
        return x;
    }
    
    /**
     * Get y
     * 
     * @return y
     */
    public int getY() {
        return y;
    }

    /**
     * Get r
     * 
     * @return r
     */
    public int getR() {
        return r;
    }

}
